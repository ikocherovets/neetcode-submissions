/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
/*
/*
/*
0. Чи потрібна поточному вузлу інформація від батьків?

   Так.

   Поточний вузол повинен знати максимальне значення,
   яке зустрілось на шляху від початкового root до нього.

   Тому контракт стає:

        dfs(root, curMaxVal)

------------------------------------------------------------

1. Контракт (що робить dfs для поточного вузла?)

   - перевіряє, чи поточний вузол є good node,
     використовуючи максимальне значення на шляху
     від початкового root до нього.

   - після цього оновлює максимум
     і передає його своїм дітям.

------------------------------------------------------------

2. Що повертає?

   - нічого (void).

   - відповідь накопичується
     у зовнішній змінній counter.

------------------------------------------------------------

3. Base case для поточного вузла

   if root == nil {
       return
   }

   - дійшли до кінця гілки.

------------------------------------------------------------

4. Leap of faith (що роблять діти?)

   dfs(root.Left, curMaxVal)

   - правильно порахує good nodes
     у лівому піддереві,
     якщо отримати правильний максимум.

   dfs(root.Right, curMaxVal)

   - правильно порахує good nodes
     у правому піддереві.

   Ми не думаємо,
   як вони це зроблять.
   Ми довіряємо їхньому контракту.

------------------------------------------------------------

5. Combine (що роблю для поточного вузла?)

   1. Перевіряю,
      чи поточний вузол good:

        root.Val >= curMaxVal

      Якщо так:

        counter++

   2. Оновлюю максимум
      для своїх дітей:

        curMaxVal = max(curMaxVal, root.Val)

   3. Передаю новий максимум
      у ліве та праве піддерево.

------------------------------------------------------------

Ідея задачі:

   Кожен вузол не знає,
   що було вище нього.

   Тому ми "несемо" вниз
   максимальне значення шляху.

   root
      |
      v
   currentMax

   Кожен вузол отримує
   актуальний максимум,
   перевіряє себе,
   оновлює його
   і передає дітям.
*/

func goodNodes(root *TreeNode) int {
    if root == nil {
        return 0
    }

    counter := 0

    var dfs func(*TreeNode, int)
    dfs = func(root *TreeNode, curMaxVal int) {
        if root == nil {
            return
        }

        if root.Val >= curMaxVal {
            counter++
        }

        curMaxVal = max(curMaxVal, root.Val)

        dfs(root.Left, curMaxVal)
        dfs(root.Right, curMaxVal)
    }

    dfs(root, root.Val)

    return counter
}